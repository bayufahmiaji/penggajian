    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        int id_urutan = Integer.parseInt(lblUrut.getText());
        double izin = Double.parseDouble(cbxIzin.getSelectedItem().toString());
        double alfa = Double.parseDouble(cbxAlfa.getSelectedItem().toString());
        double terlambat = Double.parseDouble(cbxTerlambat.getSelectedItem().toString());
        double kesehatan = Double.parseDouble(txtKesehatan.getText());
        double ketenagakerjaan = Double.parseDouble(txtKetenagakerjaan.getText());
        double pph = Double.parseDouble(txtPph.getText());
        double lain = Double.parseDouble(txtLain.getText());
        
        if(btnSimpan.getText().equals("Simpan")){
            if(controllerTransaksi.insertTransaksi(id_urutan, izin, alfa, terlambat, kesehatan, ketenagakerjaan, pph, lain)){
                setTableTransaksi(controllerTransaksi.getAllData());
                outCommand.setText("Insert Berhasil");
            } else {
                outCommand.setText("Insert Gagal");
            }
        } else {
            if(controllerTransaksi.updateTransaksi(id_urutan, izin, alfa, terlambat, kesehatan, ketenagakerjaan, pph, lain)){
                setTableTransaksi(controllerTransaksi.getAllData());
                outCommand.setText("Update Berhasil");
            } else {
                outCommand.setText("Update Gagal");
            }
        }
        setClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblTransaksiMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransaksiMousePressed
       if(evt.getClickCount()==2){
           int idxrow = tblTransaksi.getSelectedRow();
           lblUrut.setText(tblTransaksi.getModel().getValueAt(idxrow, 0).toString());
           cbxIzin.setSelectedItem(tblTransaksi.getModel().getValueAt(idxrow, 1).toString());
           cbxAlfa.setSelectedItem(tblTransaksi.getModel().getValueAt(idxrow, 2).toString());
           cbxTerlambat.setSelectedItem(tblTransaksi.getModel().getValueAt(idxrow, 3).toString());
           txtKesehatan.setText(tblTransaksi.getModel().getValueAt(idxrow, 4).toString());
           txtKetenagakerjaan.setText(tblTransaksi.getModel().getValueAt(idxrow, 5).toString());
           txtPph.setText(tblTransaksi.getModel().getValueAt(idxrow, 6).toString());
           txtLain  .setText(tblTransaksi.getModel().getValueAt(idxrow, 7).toString());
           btnSimpan.setText("Update");
       }
       if(evt.isPopupTrigger()){
           popup.show(tblTransaksi, evt.getX(), evt.getY());
       }
    }//GEN-LAST:event_tblTransaksiMousePressed

